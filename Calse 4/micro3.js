let palabra = "perro"

switch (palabra) {
    case "Perro":
        console.log("Dog");
        break;
    case "Gato":
        console.log("Cat");
        break;
    case "Ventana":
        console.log("Window");
        break;
    case "Mesa":
        console.log("Table");
        break;
    default:
        console.log("La palabra ingresada es incorrecta.");
        break;
}